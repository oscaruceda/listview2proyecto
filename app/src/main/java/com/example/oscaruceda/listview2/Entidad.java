package com.example.oscaruceda.listview2;

public class Entidad {
    private int imgFoto;
    private String titulo;


    public Entidad(int imgFoto, String titulo) {
        this.imgFoto = imgFoto;
        this.titulo = titulo;
    }

    public int getImgFoto() {
        return imgFoto;
    }

    public String getTitulo() {
        return titulo;
    }



}
